    public void showData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Kode Service");
        model.addColumn("Kode Jenis Mobil");
        model.addColumn("Jenis Service");
        model.addColumn("Biaya");
        tbl_layanan.setModel(model);
        
       
        try {
            int no=1;
            String sql = "select * from jenislayananservice";
            java.sql.Connection conn;
            conn = (Connection)Koneksi.getConnection();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),res.getString(4)});
            }
            tbl_layanan.setModel(model);
        } catch (SQLException e) {
             System.out.println("Error : " + e.getMessage());
        }
    }
    
